
using System;
class Program
    {
        static void Main(string[] args)
        {
            // Variaveis são : tipo nome = valor;

            // Inteiro
            int n1 = 10;
            Console.WriteLine(n1);
            
            // Strings
            string texto = "Olá mundo";
            Console.WriteLine(texto);

            // Boolean
            bool valor = false;
            Console.WriteLine(valor);

            // Sem tipo especifico
            var valorIndependente = 10;
            Console.WriteLine(valorIndependente);
        }
    }